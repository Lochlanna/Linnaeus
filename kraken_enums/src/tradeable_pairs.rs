impl TradeablePair {
    pub fn to_pair(&self) -> Pair {
        self.clone().into()
    }

    pub fn from_pair(pair: Pair) -> Option<Self> {
        match pair.try_into() {
            Ok(tp) => Some(tp),
            Err(_) => None
        }
    }
}
